    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            // TODO add your handling code here:
            String itemSeleccionado = ((String) cbOpcionesBusqueda.getSelectedItem()).trim();
            clienteController = new ClienteController();
            List<Cliente> listadoClientees;

            switch (itemSeleccionado) {
                case "Por nombre":
                    listadoClientees = clienteController.getAllObjectsByName(txtBuscar.getText().trim());
                    grillaClientes1.setClientees(listadoClientees);
                    break;
                case "Por telefono":
                    listadoClientees = clienteController.getAllObjectsByPhone(txtBuscar.getText().trim());
                    grillaClientes1.setClientees(listadoClientees);
                    break;
                case "Por email":
                    listadoClientees = clienteController.getAllObjectsByEmail(txtBuscar.getText().trim());
                    grillaClientes1.setClientees(listadoClientees);
                    break;
                default:
                    throw new AssertionError();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(IntFrCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(IntFrCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed



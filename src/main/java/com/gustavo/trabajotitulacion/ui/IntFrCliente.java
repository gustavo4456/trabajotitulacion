    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            // TODO add your handling code here:
            boolean estanVacios = txtDireccion.getText().trim().equals("") && txtEmail.getText().trim().equals("") && txtTelefono.getText().trim().equals("") && txtNombre.getText().trim().equals("");

            clienteController = new ClienteController();

            int filaSeleccionada = tbClientes.getSelectedRow();

            if (paraGuardar) {
                if (!estanVacios) {
                    Cliente clienteNuevo = new Cliente();
                    clienteNuevo.setDireccion(txtDireccion.getText().trim());
                    clienteNuevo.setEmail(txtEmail.getText().trim());
                    clienteNuevo.setNombre(txtNombre.getText().trim());
                    clienteNuevo.setTelefono(txtTelefono.getText().trim());

                    clienteController.insertObject(clienteNuevo);
                    llenarTabla();
                    habilitarTextBox(false);
                    habilitarBotones(true, true, false, false);
                    limpiarTextBox();
                    lblTipoOperacion.setText("");

                    notificarClienteConfirmada();
                } else {
                    JOptionPane.showMessageDialog(null, "Complete todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }

            } else {
                if (!estanVacios) {
                    if (filaSeleccionada >= 0) {
                        Cliente clienteEditar = grillaClientes1.getCliente(filaSeleccionada);
                        clienteEditar.setDireccion(txtDireccion.getText().trim());
                        clienteEditar.setEmail(txtEmail.getText().trim());
                        clienteEditar.setNombre(txtNombre.getText().trim());
                        clienteEditar.setTelefono(txtTelefono.getText().trim());

                        boolean resultado = clienteController.modifiedObject(clienteEditar);
                        System.out.println("resultado = " + resultado);
                        llenarTabla();
                        habilitarTextBox(false);
                        habilitarBotones(true, true, false, false);
                        limpiarTextBox();
                        lblTipoOperacion.setText("");
                        notificarClienteConfirmada();
                    } else {
                        JOptionPane.showMessageDialog(null, "Seleccione un cliente.", "Advertencia", JOptionPane.WARNING_MESSAGE);

                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Complete todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }

            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(IntFrCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(IntFrCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


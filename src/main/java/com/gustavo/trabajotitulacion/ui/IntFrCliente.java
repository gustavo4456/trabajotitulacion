    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tbClientes.getSelectedRow();
        boolean estanVacios = txtDireccion.getText().equals("") && txtEmail.getText().equals("") && txtTelefono.getText().equals("") && txtNombre.getText().equals("");

        if (filaSeleccionada >= 0 && !estanVacios) {
            paraGuardar = false;
            habilitarTextBox(true);
            habilitarBotones(false, false, true, true);
            lblTipoOperacion.setText("Operacion: modificar cliente");
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para editar.", "Informacion", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed


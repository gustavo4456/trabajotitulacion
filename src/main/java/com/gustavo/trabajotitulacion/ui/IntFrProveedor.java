    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            // TODO add your handling code here:
            boolean estanVacios = txtDireccion.getText().trim().equals("") && txtEmail.getText().trim().equals("") && txtTelefono.getText().trim().equals("") && txtNombre.getText().trim().equals("");

            proveedorController = new ProveedorController();

            int filaSeleccionada = tbProveedores.getSelectedRow();

            if (paraGuardar) {
                if (!estanVacios) {
                    Proveedor proveedorNuevo = new Proveedor();
                    proveedorNuevo.setDireccion(txtDireccion.getText().trim());
                    proveedorNuevo.setEmail(txtEmail.getText().trim());
                    proveedorNuevo.setNombre(txtNombre.getText().trim());
                    proveedorNuevo.setTelefono(txtTelefono.getText().trim());

                    proveedorController.insertObject(proveedorNuevo);
                    llenarTabla();
                    habilitarTextBox(false);
                    habilitarBotones(true, true, false, false);
                    limpiarTextBox();
                    lblTipoOperacion.setText("");
                    notificarProveedorConfirmada();
                } else {
                    JOptionPane.showMessageDialog(null, "Complete todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }

            } else {
                if (!estanVacios) {
                    if (filaSeleccionada >= 0) {
                        Proveedor clienteEditar = grillaProveedores1.getProveedor(filaSeleccionada);
                        clienteEditar.setDireccion(txtDireccion.getText().trim());
                        clienteEditar.setEmail(txtEmail.getText().trim());
                        clienteEditar.setNombre(txtNombre.getText().trim());
                        clienteEditar.setTelefono(txtTelefono.getText().trim());

                        boolean resultado = proveedorController.modifiedObject(clienteEditar);
                        System.out.println("resultado = " + resultado);
                        llenarTabla();
                        habilitarTextBox(false);
                        habilitarBotones(true, true, false, false);
                        limpiarTextBox();
                        lblTipoOperacion.setText("");
                        notificarProveedorConfirmada();

                    } else {
                        JOptionPane.showMessageDialog(null, "Seleccione un proveedor.", "Advertencia", JOptionPane.WARNING_MESSAGE);

                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Complete todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }

            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(IntFrProveedor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(IntFrProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


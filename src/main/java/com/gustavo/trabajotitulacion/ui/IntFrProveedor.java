    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            // TODO add your handling code here:
            String itemSeleccionado = ((String) cbOpcionesBusqueda.getSelectedItem()).trim();
            proveedorController = new ProveedorController();
            List<Proveedor> listadoProveedores;

            switch (itemSeleccionado) {
                case "Por nombre":
                    listadoProveedores = proveedorController.getAllObjectsByName(txtBuscar.getText().trim());
                    grillaProveedores1.setProveedores(listadoProveedores);
                    break;
                case "Por telefono":
                    listadoProveedores = proveedorController.getAllObjectsByPhone(txtBuscar.getText().trim());
                    grillaProveedores1.setProveedores(listadoProveedores);
                    break;
                case "Por email":
                    listadoProveedores = proveedorController.getAllObjectsByEmail(txtBuscar.getText().trim());
                    grillaProveedores1.setProveedores(listadoProveedores);
                    break;
                default:
                    throw new AssertionError();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(IntFrProveedor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(IntFrProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed


